# Create a dictionary to store information about a person
person = {
    # Set the first name key to the value 'John'
    'first name': 'Afrah',
    # Set the last name key to the value 'Doe'
    'last name': 'Saleem',
    # Set the age key to the value 30
    'age': 18,
    # Set the city key to the value 'New York'
    'city': 'London'
}

# Print the value associated with the 'first name' key
print(person['first name'])
# Print the value associated with the 'last name' key
print(person['last name'])
# Print the value associated with the 'age' key
print(person['age'])
# Print the value associated with the 'city' key
print(person['city'])
